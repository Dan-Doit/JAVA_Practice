package caculator.operator;

public class Operator {
   public Operator() {
   }

   public int plus(int num1, int num2) {
      return num1 + num2;
   }
   public int minus(int num1, int num2) {
      return num1 - num2;
   }
   public int product(int num1, int num2) {
      return num1 * num2;
   }
   public int divide(int num1, int num2) {
      return num1 / num2;
   }

}

